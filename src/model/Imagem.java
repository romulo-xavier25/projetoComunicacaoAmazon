package model;

public class Imagem {
	
	private String codigoSimbolo;
	private String nomeAMI;
	private String id;
	private String source;
	private String owner;
	private String visibility;
	private String status;
	private String dataCriacao;
	public String getCodigoSimbolo() {
		return codigoSimbolo;
	}
	public void setCodigoSimbolo(String codigoSimbolo) {
		this.codigoSimbolo = codigoSimbolo;
	}
	public String getNomeAMI() {
		return nomeAMI;
	}
	public void setNomeAMI(String nomeAMI) {
		this.nomeAMI = nomeAMI;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public String getVisibility() {
		return visibility;
	}
	public void setVisibility(String visibility) {
		this.visibility = visibility;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDataCriacao() {
		return dataCriacao;
	}
	public void setDataCriacao(String dataCriacao) {
		this.dataCriacao = dataCriacao;
	}	

}
